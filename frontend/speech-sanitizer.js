/*
 * Markdown → speech-text normalizer.
 *
 * Keep this dependency-free: kotonia-desktop is a static Tauri WebView, and
 * TTS needs plain, predictable prose rather than HTML or rendered markup.
 * The visible transcript deliberately does not use this module.
 */
(function (root, factory) {
  const api = factory();
  if (typeof module === 'object' && module.exports) module.exports = api;
  root.KotoniaSpeech = api;
})(typeof globalThis !== 'undefined' ? globalThis : this, function () {
  const FENCED_CODE_RE = /(^|\n)\s*(`{3,}|~{3,})[^\n]*\n[\s\S]*?(?:\n\s*\2\s*(?=\n|$)|$)/g;
  const INLINE_CODE_RE = /`([^`\n]+)`/g;
  const IMAGE_RE = /!\[([^\]]*)\]\([^)]*\)/g;
  const LINK_RE = /\[([^\]]+)\]\((?:[^()\\]|\\.)*\)/g;
  const REFERENCE_LINK_RE = /\[([^\]]+)\]\[[^\]]*\]/g;

  function compact(value) {
    return value
      .replace(/\r\n?/g, '\n')
      .replace(/[\t ]+\n/g, '\n')
      .replace(/\n{3,}/g, '\n\n')
      .replace(/[\t ]{2,}/g, ' ')
      .trim();
  }

  /**
   * Remove syntax that is useful on screen but harmful when literally read by
   * TTS. Link labels, image alt text, and inline code content are retained.
   */
  function stripMarkdown(input) {
    let text = String(input ?? '');

    text = text.replace(FENCED_CODE_RE, (_, leading, fence) => {
      const language = fence && _.match(/(?:^|\n)\s*(?:`{3,}|~{3,})\s*([^\s`~]+)/)?.[1];
      return `${leading}${language ? `（${language} のコードは画面に表示しています）` : '（コードは画面に表示しています）'}`;
    });
    text = text.replace(IMAGE_RE, (_, alt) => (alt ? alt : '画像'));
    text = text.replace(LINK_RE, '$1').replace(REFERENCE_LINK_RE, '$1');
    text = text.replace(INLINE_CODE_RE, '$1');

    // HTML often accompanies Markdown generated by coding agents.
    text = text.replace(/<br\s*\/?>/gi, '\n');
    text = text.replace(/<[^>]+>/g, '');
    text = text.replace(/&nbsp;/gi, ' ').replace(/&amp;/gi, '&')
      .replace(/&lt;/gi, '<').replace(/&gt;/gi, '>').replace(/&quot;/gi, '"');

    // Block syntax first, then inline decoration. Do not remove ordinary
    // hyphens or underscores within Japanese/English words.
    text = text.replace(/^\s{0,3}#{1,6}\s+/gm, '');
    text = text.replace(/^\s{0,3}>\s?/gm, '');
    text = text.replace(/^\s*(?:[-+*]|\d+[.)])\s+(?:\[[ xX]\]\s*)?/gm, '');
    text = text.replace(/^\s*(?:[-*_]\s*){3,}$/gm, '');
    text = text.replace(/^\s*\|?\s*:?-{3,}:?\s*(?:\|\s*:?-{3,}:?\s*)+\|?\s*$/gm, '');
    // Table borders are visual only; retain cell boundaries as a pause.
    text = text.replace(/^\s*\|\s?/gm, '').replace(/\s*\|\s*$/gm, '');
    text = text.replace(/\|/g, '、');
    text = text.replace(/(\*\*|__)(.*?)\1/g, '$2');
    text = text.replace(/(?<!\w)(\*|_)([^\n]+?)\1(?!\w)/g, '$2');
    text = text.replace(/~~([^~\n]+)~~/g, '$1');
    text = text.replace(/\\([\\`*{}\[\]()#+.!_>~-])/g, '$1');

    return compact(text);
  }

  return { stripMarkdown };
});
